// Uppercase<StringType>
type ZH = Uppercase<'zh'>
const zh: ZH = 'ZH'


// Lowercase<StringType>
type LL = Lowercase<'LL'>
const ll: LL = 'll'


// Capitalize<StringType>
// 首字母大写
type FF = Capitalize<'i like fitness'>
const like: FF = 'I like fitness'


// Uncapitalize<StringType>
// 首字母小写
type Blow = Uncapitalize<'YOU LIKE CODING'>
const blow: Blow = 'yOU LIKE CODING' // 代码使你扭曲